/**
 * @brief 主函数 - 灰度传感器测试示例
 *
 * 测试步骤：
 * 1. 初始化系统和 OLED 显示
 * 2. 在主循环中每 100ms 读取一次灰度传感器
 * 3. 在 OLED 上显示：
 *    - 第1行：传感器原始状态（二进制格式）
 *    - 第2行：传感器原始状态（十六进制格式）
 *    - 第3行：计算出的误差值
 *    - 第4行：丢线状态
 *
 * 使用方法：
 * 1. 将小车放在黑线上
 * 2. 左右晃动小车，观察 OLED 显示的误差值变化
 * 3. 验证：
 *    - 小车在线中央：误差接近 0.0
 *    - 小车偏右（黑线在左）：误差为负（-3.5 ~ 0）
 *    - 小车偏左（黑线在右）：误差为正（0 ~ +3.5）
 *    - 完全丢线：显示 "LOST"，误差保持上一次的值
 */
 // 定义全局运行模式变量，防止 motor.c 编译报错
int run_mode = 0;
int main(void)
{
    // ===== 系统初始化 =====
    SYSCFG_DL_init();  // 初始化时钟、GPIO、定时器等（由 SysConfig 生成）

    // ===== OLED 初始化 =====
    OLED_Init();
    OLED_ColorTurn(0);      // 0=正常显示，1=反色显示
    OLED_DisplayTurn(0);    // 0=正常显示，1=屏幕翻转
    OLED_Clear();

    // 显示标题
    OLED_ShowString(0, 0, (u8 *)"Gray Sensor Test", 16);
    OLED_Refresh();
    delay_ms(1000);
    OLED_Clear();

    // ===== 主循环：灰度传感器测试 =====
    while (1) {
        // 1. 读取灰度传感器原始状态
        uint8_t sensor_raw = Huidu_Read_Raw();

        // 2. 计算位置误差
        float error = Huidu_Get_Error();

        // 3. 判断是否丢线
        uint8_t is_lost = Huidu_Is_Lost();

        // ===== OLED 显示 =====
        char oled_str[64];

        // 第1行：显示传感器原始状态（二进制格式）
        // 示例："Bin:00011000"（传感器3和4检测到黑线）
        sprintf(oled_str, "Bin:");
        for (int8_t i = 7; i >= 0; i--) {  // 从高位到低位显示
            if (sensor_raw & (1 << i)) {
                strcat(oled_str, "1");
            } else {
                strcat(oled_str, "0");
            }
        }
        OLED_ShowString(0, 0, (u8 *)oled_str, 16);

        // 第2行：显示传感器原始状态（十六进制格式）
        // 示例："Hex: 0x18"
        sprintf(oled_str, "Hex: 0x%02X", sensor_raw);
        OLED_ShowString(0, 16, (u8 *)oled_str, 16);

        // 第3行：显示位置误差值
        // 示例："Err: +2.5" 或 "Err: -1.0"
        if (error >= 0) {
            sprintf(oled_str, "Err:+%.1f", error);
        } else {
            sprintf(oled_str, "Err:%.1f", error);  // 负数自带负号
        }
        OLED_ShowString(0, 32, (u8 *)oled_str, 16);

        // 第4行：显示丢线状态
        if (is_lost) {
            OLED_ShowString(0, 48, (u8 *)"Status: LOST", 16);
        } else {
            OLED_ShowString(0, 48, (u8 *)"Status: OK  ", 16);
        }

        // 刷新 OLED 显示
        OLED_Refresh();

        // 延时 100ms 后继续下一次检测
        delay_ms(100);
    }
}


